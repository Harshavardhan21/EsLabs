Reset_Handler
	LDR R0,=NUM
	LDR R1,[R0]
	MOV R2,#1
	LDR R3,=RESULT
LOOP
	MUL R2,R1
	SUB R1,#1
	CMP R1,#0
	BNE LOOP
	STR R2,[R3]
STOP
	B STOP
NUM DCD 0X5
